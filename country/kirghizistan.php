                <h3>Les vallées encaissées</h3>
                <p>Cette partie du guide se concentre sur les routes qui passent dans des vallées étroites, souvent le long d'une rivière. Une information est valable pour l'ensemble des vallées : l'orientation de la route vous donne beaucoup d'informations, pour déterminer dans quelle vallée vous pouvez être mais surtout pour détermienr à quel endroit de la route vous êtes.</p>
                <p>La map d'entrainement : <a href="https://www.geoguessr.com/maps/629f7c742e7e27c565d49dab">Kyrgyzstan valleys</a>.</p>
            </div>

            <div class="finalcontent">            
            <h3>plan des vallées</h3>
            <p>Les vallées étroites se divisent en 3 catégories : la vallée de Tachkoumyr, très spécifique, les vallées principales qui sont des axes importants, et les vallées secondaires avec des petites routes.</p>
            <div class="imgdiv"><img src="../img/asia/kg/mountain_valleys.png" class="imgcontent" onclick="enlargeImg('valleys')" id="valleys"></img></div>
            </div>

            <div class="finalcontent">
                <h3>Vallée de Tachkoumyr</h3>
                <p>La vallée se reconnait très facilement à la rivière Naryn, qui est à cet endroit très large et d'un bleu profond. </p>
                <div class="imgdiv"><img src="../img/asia/kg/tachkoumyr.png" class="imgcontent"></img></div>
            </div>

            <div class="finalcontent">
                <h3>Les vallées principales</h3>
                <p>La vallée de Kara-Balta se reconnait à ses blocs de bétons uniques. </p>
                <div class="imgdiv"><img src="../img/asia/kg/karabalta.png" class="imgcontent"></img></div>
                <p>La vallée de Toktogul allie des montagnes sèches, une route bordée d'arbre, et souvent une rivière de type torrent ou l'eau à l'air de s'écouler plus rapidement que dans les autres rivières des vallées principales.</p>
                <div class="imgdiv"><img src="../img/asia/kg/toktogul.png" class="imgcontent"></img></div>
                <p>La vallée au sud de Koshkor qui mène à Naryn a des paysages très secs. Les montagnes ont de l'herbe rase et seche, les arbustes en bord de route ont peu ou pas de feuilles. La rivière est de très petite taille, parfois on ne la voit pas. La route est parfois goudronnée, parfois en terre poussiereuse/en travaux. On peut y trouver des blocs de béton classiques, qui ne sont pas spécifiques à cet endroit.</p>
                <div class="imgdiv"><img src="../img/asia/kg/koshkor.png" class="imgcontent"></img></div>          
            </div>

            <div class="finalcontent">
                <h3>Les vallées mineures</h3>
                <p>La vallée au nord de Grigorievka est facilement reconnaissable, c'est un chemin de terre le long d'un torrent rapide, dans une vallée avec des sapins. La route mène à une prairie avec un lac.</p>
                <div class="imgdiv"><img src="../img/asia/kg/grigorievka.png" class="imgcontent"></img></div>
                <div class="imgdiv"><img src="../img/asia/kg/grigorievka2.png" class="imgcontent"></img></div>
