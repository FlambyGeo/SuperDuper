            <div class="finalcontent">
                <h3>Les villes</h3>
                <p>Si vous êtes dans une grande ville avec de la neige, vous êtes soit à Karakol soit un peu sur la gauche du Oktyabr District dans Bishkek.</p>
                <p>On différencie les 2 facilement, la streetview de Karakol est fortement ensoleillée avec une montagne au sud est, alors que la streetview de Bishkek est grisatre.</p>
                <div class="imgdiv"><img src="../img/asia/kg/snow_karakol.png" class="imgcontent"></img></div>
                <div class="imgdiv"><img src="../img/asia/kg/snow_bishkek.png" class="imgcontent"></img></div>
                <p>La route au nord de karakol est également enneigée, c'est une route goudronnée orientée nord sud et bordée d'arbres.</p>
                <p>Uzgen est le seul endroit avec de la neige et le rétroviseur mal réglé.</p>
                <p>ArslanBob est un grand village, avec de la streetview neige et le rétroviseur sale/boueux. La route d'accès à Arslanbob est une petite route avec de la neige.</p>
            </div>

            
            <div class="finalcontent">
                <h3>Les autres endroits</h3>
                <p>Comme dit dans le chapitre précédent, la caractèristique de la vallée d'Al-Asha, c'est que ou que vous soyez dans la vallée, si vous regardez au nord vous avez l'impression de sortir de la zone neigeuse, si vous allez au sud vous avez l'impression d'arriver dans la zone neigeuse. Le sud de la vallée d'Al Arsha est un trekker, un sentier de randonnée assez large dans une vallée enneigée.</p> 
                <div class="imgdiv"><img src="../img/asia/kg/alashanord.png" class="imgcontent"></img></div>
                <div class="imgdiv"><img src="../img/asia/kg/alashasud.png" class="imgcontent"></img></div>
                <div class="imgdiv"><img src="../img/asia/kg/snow_alasha.png" class="imgcontent"></img></div>
                <p>Chukurchak est un hameau de montagne avec une route d'accès en terre qui fait de nombreux lacets.L'endroit ressemble assez au sud du lac Song-kul.</p>
                <div class="imgdiv"><img src="../img/asia/kg/snow_chukurchak.png" class="imgcontent"></img></div>
                <p>Le sud du lac Song-kul est une route non goudronnée, avec des virages en épingle au sud puis une partie plus plate au nord. Il faut l'avoir joué une ou deux fois pour voir les différences avec Chukurchak, il n'y a pas d'habitation et la neige est un peu plus éparse.</p>
                <p>La route aux abords du lac Sary Chelek est une route non déneigée, on roule directement sur la neige.</p>
