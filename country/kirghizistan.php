            <div class="finalcontent">
                <h3>Les lacs</h3>
                <p>Il y a 3 lacs immenses, deux lac moyens, et 1 lac minuscule (en tout cas je ne connais pas d'autre lac minuscule).</p>
                <div class="imgdiv"><img src="../img/asia/kg/lake.png" class="imgcontent" onclick="enlargeImg('lake')" id="lake"></img></div>
                <p>La map d'entrainement : <a href="https://www.geoguessr.com/maps/62c7bc14ea52a36bd1d14fb8">Kyrgyzstan lakes</a>. Il y a volontairement très peu de points pour que vous puissiez tomber sur les lacs "rares" peu streetviewés.</p>
                <p></p>
              
            </div>
            <div class="finalcontent">
                <h3>Iessuk Kul</h3>
                <p>Le lac est immense, si vous le voyez vous ne pouvez pas avoir de doute. La difficulté est plutot lorsqu'on se trouve sur la route autour du lac à des endroits où on ne le voit pas depuis la route. Typiquement, la route au nord du lac est bordée d'arbres mais la zone peut se confondre avec les alentours de Bishkek. Dans ces cas là, je ne connais pas d'astce spécifique, il faut compter sur son expérience et chercher des indices (bouger pour voir le lac, chercher si on voit marquer Iessuk kul ou un nom de localité qu'on reconnaît.).</p>
            </div>

            <div class="finalcontent">
                <h3>Toktogul</h3>
                <p>Toktogul n'est pas un lac naturel (Kul = Lac), c'est le réservoir du plus grand barrage de la région. Au sud du lac, on trouve une route sinueuse bordée de blocs de bétons, lorsqu'on aperçoit la rive du lac on voit clairement qu'il n'est pas rempli à son max.</p>
                <div class="imgdiv"><img src="../img/asia/kg/toktogul_south.png" class="imgcontent"></img></div>
                <div class="imgdiv"><img src="../img/asia/kg/toktogul_up.png" class="imgcontent"></img></div>
                <p>La zone nord du lac est une route nord ouest goudronée bordée d'arbres qui mène à la ville de Toktogul.On ne voit pas le lac depuis cette route.</p>
            </div>

            <div class="finalcontent">
                <h3>Song Kul</h3>
                <p>Seul le sud du la Song Kul est streetviewé, c'est une route en terre d'où on voit le lac au loin, il faut zoomer pour bien voir que c'est un lac.</p>
                <div class="imgdiv"><img src="../img/asia/kg/songkul.png" class="imgcontent"></img></div>
            </div>

            <div class="finalcontent">
                <h3>Orto Tokoy</h3>
                <p>Comme Toktogul, il s'agit d'un réservoir qui n'est pas rempli (on voit une zone blanche autour du lac). Sur la route, on voit régulièrement les bollards blancs et noirs en béton. Si jamais vous avez un doute entre Toktogul et Orto Tokoy, vérifiez le rétroviseur (mal nettoyé = Toktogul, normal = Orto Tokoy).</p>
                <div class="imgdiv"><img src="../img/asia/kg/ortotokoy.png" class="imgcontent"></img></div>
            </div>

